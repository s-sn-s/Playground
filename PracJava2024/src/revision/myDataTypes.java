package revision;

public class myDataTypes{
	
	public static void main(String[] args) {
		
		byte b = 1;
		
		System.out.println(b);
		
		/*
		byte 		- 1Byte	= 8bit
		short 	- 2B	= 16b
		int		- 4B	= 32b
		long		- 8B	= 64b
		float		- 4B	= 32b
		double	- 8B	= 64b
		boolean	- ~1b
		char		- 2B
		char should be written inside a single quote
		char are from numeric family 
		so if any mathematical operation is performed 
		it results in ASCII value of char used
		String
		String should be written inside a double quote
		*/
		
		System.out.println("Hello " + "World!!");
		
		
	}
}